    private void jButtonCreateUpgradeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCreateUpgradeActionPerformed
        // TODO add your handling code here:
        int id = -1;
        if(!(productoReceived == null)){
            id   = productoReceived.getId(); 
        }   
        if(!(jTextNombre.getText().isEmpty()) && !(jTextCreadorNombre.getText().isEmpty()) && checkNumeric()){

            String        nombre   = jTextNombre.getText(); 
            int           anio     = (int)Double.parseDouble(jTextAnio.getText());
            double        rating   = Double.parseDouble(jTextRating.getText());
            
            CreadorModel  creador  = new CreadorModel(jComboBoxSelectedCreador.getSelectedIndex()-1>0 ? creadores.get(jComboBoxSelectedCreador.getSelectedIndex()-1).getId():-1,
                    jTextCreadorNombre.getText(), jTextCreadorNacionalidad.getText(), (int)Double.parseDouble(jTextCreadorEdad.getText()));
            String        tipo     = (String)jComboBoxTipo.getSelectedItem();
            int           durTemp  = (int)Double.parseDouble(jTextDuracionTemporadas.getText());
            if(jComboBoxTipo.getSelectedIndex() == 1){ //1 Seriie 0 Pelicula
                int episodios = (int)Double.parseDouble(jTextEpisodios.getText());
            
                SerieModel newSerie = new SerieModel(id, nombre, anio, rating, durTemp, creador, episodios, tipo);
                
                initialController.setSerieDAO(newSerie);
            }else{
                String resumen = jTexPaneResumenEpisodios.getText();
                PeliculaModel newPelicula = new PeliculaModel(id, nombre, anio, rating, durTemp, creador, resumen, tipo);
                initialController.setPeliculaDAO(newPelicula);
            //  PeliculaModel newPelicula = new PeliculaModel();
            }
            MainClassModel.setVista();
        }
        
    }//GEN-LAST:event_jButtonCreateUpgradeActionPerformed


    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
       creadores = initialController.getFilteredCreadorDAO("");
       jComboBoxSelectedCreador.removeAllItems();
       jComboBoxSelectedCreador.addItem("Nuevo Creador");
       for(int i=0; i<creadores.size(); i++){
           
           jComboBoxSelectedCreador.addItem(creadores.get(i).getNombre());
       }
       
       if(button.equals("Open")){
           
           jComboBoxTipo.setVisible(false);
           creador = productoReceived.getCreador();
           jComboBoxSelectedCreador.setSelectedIndex(creador.getId());
           /*parametros editables false en crador
           jTextCreadorEdad.setEditable(false);
           jTextCreadorNombre.setEditable(false);
           jTextCreadorNacionalidad.setEditable(false);
           */
           
           jButtonCreateUpgrade.setText("Actualizar");
           //set creador
           jTextCreadorNombre.setText(creador.getNombre());;
           jTextCreadorEdad.setText(""+creador.getEdad());
           jTextCreadorNacionalidad.setText(creador.getNacionalidad());
           //set producto
            if(productoReceived.getTipo().equals("Serie")){
                jTexPaneResumenEpisodios.setVisible(false);
                jTextEpisodios.setVisible(true);
                
                jComboBoxTipo.setSelectedIndex(1);
                jLabelTipo.setText("SERIE");
                jLabelDuracionTemporadas.setText("Temporadas:");
                jLabelResumenEpisodios.setText("Episodios:");
                
                
                this.serie = initialController.getSerie(productoReceived);
                
                jTextNombre.setText(serie.getNombre());
                jTextAnio.setText(""+serie.getAnio());
                jTextRating.setText(""+serie.getRating());
                jTextDuracionTemporadas.setText(""+serie.getTemporadas());
                jTexPaneResumenEpisodios.setText("");
                
                jTextEpisodios.setText(""+serie.getEpisodios());
                
                
            }else{
                
                jComboBoxTipo.setSelectedIndex(0);
                jLabelTipo.setText("PELICULA");
                jLabelDuracionTemporadas.setText("Duración:");
                jLabelResumenEpisodios.setText("Resumen:");
                
                this.pelicula = initialController.getPelicula(productoReceived);
                jTextEpisodios.setVisible(false);
                jTexPaneResumenEpisodios.setVisible(true);
                
                jTextNombre.setText(pelicula.getNombre());
                jTextAnio.setText(""+pelicula.getAnio());
                jTextRating.setText(""+pelicula.getRating());
                jTextDuracionTemporadas.setText(""+pelicula.getDuracion());
                jTextEpisodios.setText("");
                
                jTexPaneResumenEpisodios.setText(""+pelicula.getResumen());
                
                
            }
       }else{
           jComboBoxSelectedCreador.setSelectedIndex(0);
           jComboBoxTipo.setSelectedIndex(0);
           jComboBoxTipo.setVisible(true);
           jTextEpisodios.setVisible(false);
                jTexPaneResumenEpisodios.setVisible(true);
           //jComboBoxTipo.setEditable(true);
           jButtonCreateUpgrade.setText("Crear");
           jButtonDelete.setVisible(false);
           jLabelTipo.setText("Seleccione Tipo");
           
           //set creador
           jTextCreadorNombre.setText("");;
           jTextCreadorEdad.setText("");
           jTextCreadorNacionalidad.setText("");
           jComboBoxSelectedCreador.setSelectedIndex(0);
                jLabelDuracionTemporadas.setText("");
                jLabelResumenEpisodios.setText("");
                
                jLabelDuracionTemporadas.setText("Duración:");
                jLabelResumenEpisodios.setText("Resumen:");
                
                jTextNombre.setText("");
                jTextAnio.setText("");
                jTextRating.setText("");
                jTextDuracionTemporadas.setText("");
                 jTextEpisodios.setText("");
                
                
                jTexPaneResumenEpisodios.setText("");
           
       
       }       
    }//GEN-LAST:event_formWindowActivated


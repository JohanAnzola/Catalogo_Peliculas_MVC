    private void ShowResults(){
        //Borra los datos de la tabla
        int rowDeleted = modelTableProducto.getRowCount();
        for(int i=rowDeleted-1; i>=0; i-- ){
            modelTableProducto.removeRow(i);
        }
        int tipoSelected = jComboBoxTipo.getSelectedIndex();
        //pelis crea y agrega
        //Crea obeto de control donde llamaremos los metodos
        
        
        ArrayList<ProductoModel> productos = new ArrayList();
   
        
        // declaración de switch caso select tipo
        switch(tipoSelected){
            case 1 : //peliculas
                productos = initialController.getProductoPeliculas(jTextBuscar.getText());
                break; // 
            case 2 : //Series
                productos = initialController.getProductoSeries(jTextBuscar.getText());
                break; // b
            default : //peliculas y series
                productos = initialController.getAllProductos(jTextBuscar.getText());
                
        }
        for(int i=0; i<productos.size(); i++){
                    modelTableProducto.addRow(productos.get(i).toArray() );
        }
       
    }
 


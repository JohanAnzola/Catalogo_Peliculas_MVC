    private void jButtonBorrarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarProductoActionPerformed
        // TODO add your handling code here:
        
        if(jTableProducto.getSelectedRow() != -1){
            int resp = JOptionPane.showConfirmDialog(null, "Está seguro que desea eliminar el registro",
                "", JOptionPane.YES_NO_OPTION,
                JOptionPane.INFORMATION_MESSAGE);
            if(resp == 0){
                initialController.getProducto(jTableProducto.getSelectedRow());
                initialController.deleteProducto(jTableProducto.getSelectedRow());
                jTextBuscar.setText("");
                jComboBoxTipo.setSelectedIndex(0);      
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Selecione el producto que desea Borrar");
        }
    }//GEN-LAST:event_jButtonBorrarProductoActionPerformed

